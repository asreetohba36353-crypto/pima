        "‡∏°‡∏µ‡∏Ñ‡∏ô‡πÉ‡∏ô‡∏Ñ‡∏£‡∏≠‡∏ö‡∏Ñ‡∏£‡∏±‡∏ß‡∏™‡∏≤‡∏¢‡∏ï‡∏£‡∏á (‡∏û‡πà‡∏≠/‡πÅ‡∏°‡πà)",
        "‡∏°‡∏µ‡∏´‡∏•‡∏≤‡∏¢‡∏Ñ‡∏ô‡πÉ‡∏ô‡∏Ñ‡∏£‡∏≠‡∏ö‡∏Ñ‡∏£‡∏±‡∏ß",
        "‡∏û‡∏±‡∏ô‡∏ò‡∏∏‡∏Å‡∏£‡∏£‡∏°‡πÅ‡∏£‡∏á‡∏°‡∏≤‡∏Å"
